import type { League, Club } from "@/types";

function mk(
  country: string,
  league: string,
  countryCode: string,
  raw: Array<[string, string, number]>, // [name, slug, tier]
): Club[] {
  return raw.map(([name, slug, tier]) => ({
    id: `${countryCode}-${slug}`,
    name,
    shortName: name,
    country,
    league,
    tier,
    logoSlug: slug,
  }));
}

