export function getLeagueByCountry(code: string): League {
  const league = LEAGUES[code];
  if (!league) throw new Error(`No league for country: ${code}`);
  return league;
}

export function allClubs(): Club[] {
  return Object.values(LEAGUES).flatMap((l) => l.clubs);
}

export function clubById(id: string): Club | undefined {
  for (const l of Object.values(LEAGUES)) {
    const c = l.clubs.find((x) => x.id === id);
    if (c) return c;
  }
  return undefined;
}

export function leagueByName(name: string): League | undefined {
  return Object.values(LEAGUES).find((l) => l.name === name);
}
